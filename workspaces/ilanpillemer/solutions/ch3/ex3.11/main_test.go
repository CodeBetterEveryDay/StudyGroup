package main

import "testing"

func TestComma(t *testing.T) {

	tests := []struct {
		input string
		want  string
	}{
		{"-123", "-123"},
		{"12345", "12,345"},
		{"-123456", "-123,456"},
		{"1234567", "1,234,567"},
		{"-12345678", "-12,345,678"},
		{"123456789", "123,456,789"},
		{"123456789e-07", "123,456,789E-07"},
		{"123456789e07", "123,456,789E07"},
		{"12345678.00001", "12,345,678.00001"},
		{"-12345678.00001", "-12,345,678.00001"},
		{"-123456.00000006", "-123,456.00000006"},
		{"-123456.00000006e-07", "-123,456.00000006E-07"},
		{"-123456.00000006e07", "-123,456.00000006E07"},
		{"-123456.00000006e07", "-123,456.00000006E07"},
		{"123456.00000006e07", "123,456.00000006E07"},
	}

	for _, tst := range tests {

		got := comma(tst.input)
		if got != tst.want {
			t.Errorf("got %s, want %s", got, tst.want)
		}
	}

}
